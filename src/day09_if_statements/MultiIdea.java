package day09_if_statements;

public class MultiIdea {
    public static void main(String[] args) {

         /*
        number -> month
        1 jan
        2 feb
        12 dec
         */

        int month = 1;

        if(month == 1){
            System.out.println("month is January");
        }

        if(month == 2){
            System.out.println("February");
        }

        if(month == 12) {
            System.out.println("December");


        }




        }






}
