public class OutSide {
    public static void main(String[] args) {

        double degrees = 80;

        if (degrees >= 72){
            System.out.println("It's nice, go outside");
        }
        if (degrees < 72){
            System.out.println("It's nice, go outside");
        }
        System.out.println("----------");
        if (degrees >= 72){
            System.out.println("It's nice, go outside");
        } else  {
            System.out.println("It's nice, go outside");
        }


    }
}
